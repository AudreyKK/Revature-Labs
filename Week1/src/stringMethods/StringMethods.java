package stringMethods;

public class StringMethods {

	public static void main(String[] args) {
		String str = "Hello";
		String s = new String("Hello");
		
		// equals()
		System.out.println(str.equals(s));
		
		// length()
		System.out.println(str.length());
		
		// indexOf()
		System.out.println(str.indexOf("H"));

	}

}
